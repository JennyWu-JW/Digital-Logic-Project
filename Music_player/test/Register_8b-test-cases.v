  //----------------------------------------------------------------------
  // test_case_4_directed_enable
  //----------------------------------------------------------------------
  // Test enable input

  task test_case_4_directed_enable();
    t.test_case_begin( "test_case_4_directed_enable" );

    //    rst en d             q
    check( 0, 1, 8'b0000_0000, 8'b0000_0000 ); // en=1
    check( 0, 1, 8'b0011_0011, 8'b0000_0000 );
    check( 0, 1, 8'b1100_1100, 8'b0011_0011 );

    check( 0, 0, 8'b1111_1111, 8'b1100_1100 ); // en=0
    check( 0, 0, 8'b1111_0000, 8'b1100_1100 );
    check( 0, 0, 8'b0000_1111, 8'b1100_1100 );

    check( 0, 1, 8'b1111_1111, 8'b1100_1100 ); // en=1
    check( 0, 1, 8'b1111_0000, 8'b1111_1111 );
    check( 0, 1, 8'b0000_0000, 8'b1111_0000 );

  endtask

  //----------------------------------------------------------------------
  // test_case_5_directed_reset
  //----------------------------------------------------------------------
  // Test various reset conditions

  task test_case_5_directed_reset();
    t.test_case_begin( "test_case_5_directed_reset" );

    //    rst en d             q
    check( 0, 1, 8'b0000_0000, 8'b0000_0000 );
    check( 0, 1, 8'b0011_0011, 8'b0000_0000 );
    check( 0, 1, 8'b1100_1100, 8'b0011_0011 );

    check( 1, 1, 8'b1111_1111, 8'b1100_1100 ); // rst=1, en=1
    check( 1, 1, 8'b0000_1111, 8'b0000_0000 );
    check( 1, 1, 8'b1111_1111, 8'b0000_0000 );

    check( 0, 1, 8'b1111_0000, 8'b0000_0000 );
    check( 0, 1, 8'b0011_0011, 8'b1111_0000 );
    check( 0, 1, 8'b1100_1100, 8'b0011_0011 );

    check( 1, 0, 8'b1111_1111, 8'b1100_1100 ); // rst=1, en=0
    check( 1, 0, 8'b0000_1111, 8'b0000_0000 );
    check( 1, 0, 8'b1111_1111, 8'b0000_0000 );

    check( 0, 0, 8'b1111_0000, 8'b0000_0000 );
    check( 0, 0, 8'b0011_0011, 8'b0000_0000 );
    check( 0, 0, 8'b1100_1100, 8'b0000_0000 );

  endtask
